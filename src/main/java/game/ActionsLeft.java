package game;

/**
 * Class that determines how many actions are left.
 *
 * @author santorsa
 * @category Actions
 */
public class ActionsLeft {
	public int num;

	/**
	 * 0 arg constructor
	 */
	public ActionsLeft() {
	}

	/**
	 * 1 arg constructor
	 *
	 * @param num Number of actions left.
	 */
	public ActionsLeft(int num) {
		this.num = num;
	}
}
